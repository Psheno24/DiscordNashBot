export function randomJoinCopy(tag: string, guildName: string): { title: string; description: string } {
  return {
    title: pick(JOIN_TITLES),
    description: joinBody(tag, guildName),
  };
}

export function randomLeaveCopy(tag: string, guildName: string): { title: string; description: string } {
  return {
    title: pick(LEAVE_TITLES),
    description: leaveBody(tag, guildName),
  };
}
